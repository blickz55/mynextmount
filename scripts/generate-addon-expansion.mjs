/**
 * Emit addons/MyNextMount/MyNextMountExpansionData.lua from data/mounts.json.
 * Bucket rules must stay aligned with lib/mountExpansionFocus.ts (run when expansion synonyms change).
 *
 *   npm run addon:sync-expansion
 */

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

/** @type {{ id: string, synonyms: string[] }[]} */
const ERA_ROWS = [
  {
    id: "classic",
    synonyms: ["Classic", "Vanilla", "Classic Era", "Era Classic"],
  },
  {
    id: "tbc",
    synonyms: ["The Burning Crusade", "Burning Crusade", "TBC", "BC"],
  },
  {
    id: "wrath",
    synonyms: [
      "Wrath of the Lich King",
      "WotLK",
      "Wrath",
      "Lich King",
      "WOTLK",
    ],
  },
  { id: "cata", synonyms: ["Cataclysm", "Cata"] },
  {
    id: "mop",
    synonyms: ["Mists of Pandaria", "Mists", "MoP", "Pandaria"],
  },
  {
    id: "wod",
    synonyms: ["Warlords of Draenor", "Warlords", "WoD", "Draenor"],
  },
  { id: "legion", synonyms: ["Legion"] },
  {
    id: "bfa",
    synonyms: ["Battle for Azeroth", "BfA", "BFA", "Battle For Azeroth"],
  },
  {
    id: "shadowlands",
    synonyms: ["Shadowlands", "SL"],
  },
  {
    id: "dragonflight",
    synonyms: ["Dragonflight", "Dragon Flight", "DF"],
  },
  {
    id: "the_war_within",
    synonyms: ["The War Within", "War Within", "TWW"],
  },
];

function normalizeExpansionLabel(raw) {
  return String(raw)
    .trim()
    .toLowerCase()
    .replace(/['’]/g, "'")
    .replace(/\s+/g, " ");
}

function mountExpansionFocusIdFromString(expansion) {
  const raw = String(expansion ?? "").trim();
  const n = normalizeExpansionLabel(raw);
  if (!raw || n === "unknown" || n === "?" || n === "—" || n === "-") {
    return "unknown";
  }
  for (const row of ERA_ROWS) {
    for (const syn of row.synonyms) {
      if (normalizeExpansionLabel(syn) === n) return row.id;
    }
  }
  return "unknown";
}

const mounts = JSON.parse(
  readFileSync(join(root, "data", "mounts.json"), "utf8"),
);

const lines = [];
lines.push(
  "-- AUTO-GENERATED by scripts/generate-addon-expansion.mjs — do not edit.",
);
lines.push("-- Spell summon ID → era bucket (matches lib/mountExpansionFocus.ts).");
lines.push("");
lines.push("MyNextMountExpansionData = MyNextMountExpansionData or {}");
lines.push("MyNextMountExpansionData.bySpellId = {");

for (const m of mounts) {
  if (m.retailObtainable === false) continue;
  const id = Number(m.id);
  if (!Number.isFinite(id) || id <= 0) continue;
  const bucket = mountExpansionFocusIdFromString(m.expansion);
  lines.push(`  [${id}] = "${bucket}",`);
}

lines.push("}");
lines.push("");

const outPath = join(root, "addons", "MyNextMount", "MyNextMountExpansionData.lua");
writeFileSync(outPath, lines.join("\n") + "\n", "utf8");
console.log(
  `[addon:sync-expansion] Wrote ${mounts.filter((m) => m.retailObtainable !== false).length} spell map entries → ${outPath}`,
);
