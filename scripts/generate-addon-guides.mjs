/**
 * Epic C.2 — Emit addons/MountFarmExport/MountFarmGuides.lua (MyNextMount addon) from data/mount-guides.json
 * (+ mount names from data/mounts.json). Run after editing guides:
 *   npm run addon:sync-guides
 */

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

function luaStr(s) {
  return (
    '"' +
    String(s)
      .replace(/\\/g, "\\\\")
      .replace(/"/g, '\\"')
      .replace(/\r\n/g, "\n")
      .replace(/\n/g, "\\n") +
    '"'
  );
}

const guidesFile = JSON.parse(
  readFileSync(join(root, "data", "mount-guides.json"), "utf8"),
);
const mounts = JSON.parse(readFileSync(join(root, "data", "mounts.json"), "utf8"));
const nameById = new Map(mounts.map((m) => [m.id, m.name]));

const guides = guidesFile.guides || {};
const spellIds = Object.keys(guides)
  .map(Number)
  .filter((n) => Number.isFinite(n))
  .sort((a, b) => a - b);

const lines = [];
lines.push("-- AUTO-GENERATED by scripts/generate-addon-guides.mjs — do not edit.");
lines.push("-- Source: data/mount-guides.json (+ names from data/mounts.json)");
lines.push("");
lines.push("MountFarmGuideData = MountFarmGuideData or {}");
lines.push("MountFarmGuideData.order = {");
for (const id of spellIds) {
  lines.push(`  ${id},`);
}
lines.push("}");
lines.push("");
lines.push("MountFarmGuideData.byId = {");

for (const id of spellIds) {
  const g = guides[String(id)];
  if (!g) continue;
  const name = nameById.get(id) || `Spell ${id}`;
  lines.push(`  [${id}] = {`);
  lines.push(`    name = ${luaStr(name)},`);
  lines.push(`    overview = ${luaStr(g.overview || "")},`);
  lines.push(`    sourceUrl = ${luaStr(g.sourceUrl || "")},`);
  lines.push(`    sourceLabel = ${luaStr(g.sourceLabel || "Source")},`);
  lines.push(`    checklist = {`);
  for (const step of g.checklist || []) {
    lines.push(`      ${luaStr(step)},`);
  }
  lines.push(`    },`);
  lines.push(`  },`);
}

lines.push("}");
lines.push("");

const outPath = join(root, "addons", "MountFarmExport", "MountFarmGuides.lua");
writeFileSync(outPath, lines.join("\n") + "\n", "utf8");
console.log(`[addon:sync-guides] Wrote ${spellIds.length} guide(s) → ${outPath}`);
